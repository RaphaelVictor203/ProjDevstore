package br.com.devstore.model;

public class TotVendasMes {
	
	private int mes_venda;
	private int qntd_vendas;
	private double total_recebido;
	
	public int getMes_venda() {
		return mes_venda;
	}
	public void setMes_venda(int mes_venda) {
		this.mes_venda = mes_venda;
	}
	public int getQntd_vendas() {
		return qntd_vendas;
	}
	public void setQntd_vendas(int qntd_vendas) {
		this.qntd_vendas = qntd_vendas;
	}
	public double getTotal_recebido() {
		return total_recebido;
	}
	public void setTotal_recebido(double total_recebido) {
		this.total_recebido = total_recebido;
	}
	
	

}
